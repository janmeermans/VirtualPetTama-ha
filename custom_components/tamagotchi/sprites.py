# Backward-compat alias (renderer uses get_sprite which now takes creature_type)
STAGE_SPRITES = CREATURE_SPRITES["dog"]


def get_sprite(
    stage: str,
    frame: int,
    creature_type: str = "dog",
    sick: bool = False,
) -> tuple:
    """Return the sprite tuple for a given stage, frame, creature type, and sick flag."""
    creature_data = CREATURE_SPRITES.get(creature_type, CREATURE_SPRITES["dog"])
    stage_data = creature_data.get(stage, creature_data.get("egg", {}))
    key = "sick" if sick and "sick" in stage_data else "normal"
    frames = stage_data[key]
    return frames[frame % len(frames)]


def pixel(row: int, col: int) -> int:
    """Return 1 if the pixel at (row, col) is set, else 0."""
    return (row >> (15 - col)) & 1
