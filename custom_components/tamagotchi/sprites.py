# ---------------------------------------------------------------------------
# Lookup by stage name → list of frames
# ---------------------------------------------------------------------------

STAGE_SPRITES: dict[str, list[tuple]] = {
    "egg":    EGG,
    "baby":   BABY,
    "child":  CHILD,
    "teen":   TEEN,
    "adult":  ADULT,
    "senior": SENIOR,
    "dead":   DEAD,
}


def get_sprite(stage: str, frame: int) -> tuple:
    frames = STAGE_SPRITES.get(stage, EGG)
    return frames[frame % len(frames)]


def pixel(row: int, col: int) -> int:
    """Return 1 if pixel at (row, col) is set, else 0."""
    return (row >> (15 - col)) & 1
