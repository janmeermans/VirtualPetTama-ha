"""16×16 pixel art sprites.

Encoding: each sprite is a tuple of 16 integers (one row each).
Bit 15 = column 0 (leftmost). Bit 0 = column 15 (rightmost).
pixel(row_int, col) = (row_int >> (15 - col)) & 1

Eye anatomy (child/teen/adult/elder):
  Left eye  → columns 4-6  (3 px wide, 3 rows tall)
  Nose col  → column 7
  Right eye → columns 8-10 (3 px wide, 3 rows tall)
  Sick X-eyes exploit this consistent layout across both creature types.

Dog features: floppy ears (hang down), hair tuft (teen+)
Cat features: pointed ears (stick up), visible tail on right side
"""

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _r(*cols: int) -> int:
    """Return a 16-bit row integer with bits set at the given column positions."""
    v = 0
    for c in cols:
        v |= (1 << (15 - c))
    return v

def _s(*rows) -> tuple:
    """Pack 16 row integers into a sprite tuple."""
    return tuple(rows)


