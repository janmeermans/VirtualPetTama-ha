"""16×16 pixel art sprites for each life stage.

Each sprite is a tuple of 16 integers (one per row).
Bit 15 of each integer = column 0 (leftmost pixel).
Bit 0 = column 15 (rightmost pixel).

Use pixel(row_int, col) = (row_int >> (15 - col)) & 1
"""

# ---------------------------------------------------------------------------
# Helper
# ---------------------------------------------------------------------------

def _r(*cols: int) -> int:
    """Build a 16-bit row integer from a list of set column indices (0-based)."""
    val = 0
    for c in cols:
        val |= (1 << (15 - c))
    return val


def _s(*rows) -> tuple:
    """Sprite from rows passed as integers or _r() calls."""
    return tuple(rows)


